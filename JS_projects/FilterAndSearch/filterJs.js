for(let item of products.data){
    let card  = document.createElement('div');
    card.classList.add('card',item.category.toLowerCase(),'hide');
    let imgContainer = document.createElement('div');
    imgContainer.classList.add("imgContainer");
    let image = document.createElement('img');
    image.setAttribute('src',item.image);
    imgContainer.appendChild(image);
    card.appendChild(imgContainer);
    let container = document.createElement('div');
    container.classList.add('container');
    let name = document.createElement('h5');
    name.classList.add("nameProduct");
    name.innerText = item.productName;
    container.appendChild(name);

 let price = document.createElement('h6');
    price.classList.add("priceProduct");
    price.innerText = "$" + item.price;
    container.appendChild(price);

    card.appendChild(container)
    document.getElementById('items').appendChild(card);
}
function filterItems(value){
let buttons = document.querySelectorAll('.btnAll');
buttons.forEach((x)=>{
        if(value.toLowerCase()==x.innerText.toLowerCase().replace(/\s/g, '')){
                x.classList.add('active')
        }
        else{
               x.classList.remove('active')
        }
        });
        let elements = document.querySelectorAll('.card')
        elements.forEach(e=>{
                if(value=="all"){
                    e.classList.remove("hide");    
                }
                else{
                        if(e.classList.contains(value.toLowerCase())){
                                   e.classList.remove("hide");     
                        }
                        else{
                                e.classList.add("hide");    
                        }
                }
        })

}

document.getElementById('searchButton').addEventListener("click",()=>{
        let search=document.getElementById("searchInput").value;
        let elements = document.querySelectorAll('.nameProduct');
        let cards= document.querySelectorAll('.card');
        elements.forEach((e,index)=>{
if(e.innerText.toLowerCase().includes(search.toLowerCase())){
        cards[index].classList.remove('hide')
}
else{
         cards[index].classList.add('hide')
}
        })
})

window.onload=()=>{
        filterItems("all");
}